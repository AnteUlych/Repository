package lotos.model;

public class CompanyForStatistic {
	
	private String company;
	private String registration;
	private int numberOfGoodComments;
	private int numberOfBadComments;
	private int numberOfPropositionDeals;
	private int numberOfTenderDeals;
	private String code;
	private String manager;
	private String mail;
	private String phone;
	private String mobile;
	
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getRegistration() {
		return registration;
	}
	public void setRegistration(String registration) {
		this.registration = registration;
	}
	public int getNumberOfGoodComments() {
		return numberOfGoodComments;
	}
	public void setNumberOfGoodComments(int numberOfGoodComments) {
		this.numberOfGoodComments = numberOfGoodComments;
	}
	public int getNumberOfBadComments() {
		return numberOfBadComments;
	}
	public void setNumberOfBadComments(int numberOfBadComments) {
		this.numberOfBadComments = numberOfBadComments;
	}
	public int getNumberOfPropositionDeals() {
		return numberOfPropositionDeals;
	}
	public void setNumberOfPropositionDeals(int numberOfPropositionDeals) {
		this.numberOfPropositionDeals = numberOfPropositionDeals;
	}
	public int getNumberOfTenderDeals() {
		return numberOfTenderDeals;
	}
	public void setNumberOfTenderDeals(int numberOfTenderDeals) {
		this.numberOfTenderDeals = numberOfTenderDeals;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getManager() {
		return manager;
	}
	public void setManager(String manager) {
		this.manager = manager;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	
}
