package box.model;

public class ClientForRouteHTML {
	
	private int id;
	private String company;
	private String blacklist;
	private String typetruck;
	private String cargo;
	private String posibilityToGo;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getBlacklist() {
		return blacklist;
	}
	public void setBlacklist(String blacklist) {
		this.blacklist = blacklist;
	}
	public String getTypetruck() {
		return typetruck;
	}
	public void setTypetruck(String typetruck) {
		this.typetruck = typetruck;
	}
	public String getCargo() {
		return cargo;
	}
	public void setCargo(String cargo) {
		this.cargo = cargo;
	}
	public String getPosibilityToGo() {
		return posibilityToGo;
	}
	public void setPosibilityToGo(String posibilityToGo) {
		this.posibilityToGo = posibilityToGo;
	}

}
