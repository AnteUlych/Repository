package box.model;

public class Report {
	
	private String manager;
	private int propositions;
	private int deals;
	private int eur;
	private int usd;
	private int uah;
	private String linkPropositions;
	private String linkWinPropositions;
	
	public String getManager() {
		return manager;
	}
	public void setManager(String manager) {
		this.manager = manager;
	}
	public int getPropositions() {
		return propositions;
	}
	public void setPropositions(int propositions) {
		this.propositions = propositions;
	}
	public int getDeals() {
		return deals;
	}
	public void setDeals(int deals) {
		this.deals = deals;
	}
	public int getEur() {
		return eur;
	}
	public void setEur(int eur) {
		this.eur = eur;
	}
	public int getUsd() {
		return usd;
	}
	public void setUsd(int usd) {
		this.usd = usd;
	}
	public int getUah() {
		return uah;
	}
	public void setUah(int uah) {
		this.uah = uah;
	}
	public String getLinkPropositions() {
		return linkPropositions;
	}
	public void setLinkPropositions(String linkPropositions) {
		this.linkPropositions = linkPropositions;
	}
	public String getLinkWinPropositions() {
		return linkWinPropositions;
	}
	public void setLinkWinPropositions(String linkWinPropositions) {
		this.linkWinPropositions = linkWinPropositions;
	}



}
